    }

    /**
     * Store a newly created resource in storage.
     */
    public function store()
    {
        $att = request()->validate([
            'name' => ['required', 'min:3'],
            'email' => ['required','email'],
            'password' => ['required'],
           
        ]);
    
       $user = User::create($att);

       Auth::login($user);

       return redirect('/jobs');
    }

    /**
     * Display the specified resource.
